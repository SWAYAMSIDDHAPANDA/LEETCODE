// Problem: 2235_Add Two Integers
// Link: https://leetcode.com/problems/add-two-integers/

// Approach:
// Simply return the sum of the two given integers.

// Time Complexity: O(1)

// Space Complexity: O(1)

class Solution {
    public int sum(int num1, int num2) {
        return (num1 + num2);
    }
}
